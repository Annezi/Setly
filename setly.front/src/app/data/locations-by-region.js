const QUERY_MIN_LENGTH = 1;
const normalizeQuery = (q) => (q || "").toLowerCase().trim();

/**
 * Фильтрует локации по введённой строке: показываются только те, у которых
 * строка целиком или любая часть (город/страна до или после запятой) начинается с запроса.
 * @param {string} query
 * @returns {{ label: string, region: string }[]}
 */
export function filterLocations(query) {
	const nq = normalizeQuery(query);
	if (nq.length < QUERY_MIN_LENGTH) return [];
	return LOCATIONS_BY_REGION.filter((item) => {
		const normalizedLabel = normalizeQuery(item.label);
		if (normalizedLabel.startsWith(nq)) return true;
		const parts = normalizedLabel.split(",").map((p) => p.trim());
		return parts.some((part) => part.startsWith(nq));
	});
}
